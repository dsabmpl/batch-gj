public class Loops {
    public static void main(String[] args) {
        System.out.println("Yash");
        System.out.println("Yash");
        System.out.println("Yash");
        System.out.println("Yash");
        System.out.println("Yash");
        // For Loop
        // While Loop
        // Do while

        for (int i = 0; i < 10; i++) {
            System.out.println("Yash " + i);
        }
        int i = 0;
        while (i < 10) {
            System.out.println("While " + i);
            i++;
        }
        i = 0;
        do {
            System.out.println("Do while");
            i++;
        } while (i < 10);

    }

}
